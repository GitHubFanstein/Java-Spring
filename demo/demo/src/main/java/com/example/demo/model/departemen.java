package com.example.demo.model;

// class that represents model
public class departemen {
    
    // declare variables
    int departemenID;
    String departemenName;
    String locationDepartemen;

    // define getter and setter
    public int getDepartemenID() {
        return departemenID;
    }
    public void setDepartemenID(int departemenID) {
        this.departemenID = departemenID;
    }
    public String getDepartemenName() {
        return departemenName;
    }
    public void setDepartemenName(String departemenName) {
        this.departemenName = departemenName;
    }
    public String getLocationDepartemen() {
        return locationDepartemen;
    }
    public void setLocationDepartemen(String locationDepartemen) {
        this.locationDepartemen = locationDepartemen;
    }

    
}
